// ==UserScript==
// @run-at document-start
// ==/UserScript==

// Ensure that so-called "smart banners" generated by the smartbanner jQuery
// plugin (http://jasny.github.io/jquery.smartbanner/) that advertise native
// Android/iOS applications based on a very na√Øve parsing of the UA string
// are never shown.

var exdate = new Date();
exdate.setFullYear(exdate.getFullYear() + 5);
try {
    document.cookie = "sb-closed=true;path=/;expires=" + exdate.toUTCString();
} catch (e) {}
